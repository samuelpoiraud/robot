{
	if(d0 > 32 || d1 > 32)
	{
		*x = BAD;
		*y = BAD;
		return FALSE;		//sécurité !
	}	
	unsigned psv_shadow;
	psv_shadow = PSVPAG;
	PSVPAG = __builtin_psvpage (array_x_from_d0_d1);
	*x = array_x_from_d0_d1[d0][d1];
	PSVPAG = __builtin_psvpage (array_y_from_d0_d1);
	*y = array_y_from_d0_d1[d0][d1];
	PSVPAG = psv_shadow;
	if(*x == BAD || *y == BAD)
		return FALSE;
	return TRUE;
}




