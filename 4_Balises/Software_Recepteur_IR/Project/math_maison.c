/* fonction qui "trouve" la valeur de cosinus et sinus de x en radians
et compris entre 0 et 2*pi  */
void cos_sin (Uint16 x,Sint16 * cos, Sint16 * sin){
	Sint16 xprim;
	// cosinus
	if (x<HALF_PI/2){
		cosinus(x,cos);
		sinus(x,sin);
	}
	else if(x<HALF_PI){
		xprim=(Sint16)((HALF_PI-x));
		sinus(xprim,cos);
		cosinus(xprim,sin);
	}
	else if(x<3*HALF_PI/2){
		xprim=(Sint16)((x-HALF_PI));
		sinus(xprim,cos);
		*cos=-*cos;
		cosinus(xprim,sin);
	}
	else if(x<PI4096){
		xprim=(Sint16)((PI4096-x));
		cosinus(xprim,cos);
		*cos=-*cos;
		sinus(xprim,sin);
	}
	else if(x<5*HALF_PI/2){
		xprim=(Sint16)((x-PI4096));
		cosinus(xprim,cos);
		*cos=-*cos;
		sinus(xprim,sin);
		*sin=-*sin;
	}
	else if(x<3*HALF_PI){
		xprim=(Sint16)((3*HALF_PI-x));
		sinus(xprim,cos);
		*cos=-*cos;
		cosinus(xprim,sin);
		*sin=-*sin;
	}
	else if(x<7*(HALF_PI/2)){
		xprim=(Sint16)((x-3*HALF_PI));
		sinus(xprim,cos);
		cosinus(xprim,sin);
		*sin=-*sin;
	}
	else{
		xprim=(Sint16)((TWO_PI-x));
		cosinus(xprim,cos);
		sinus(xprim,sin);
		*sin=-*sin;
	}
}

void cosinus(Sint16 x,Sint16*cos){
	unsigned psv_shadow;
	psv_shadow = PSVPAG;
	PSVPAG = __builtin_psvpage (cosr);
	*cos=cosr[x];
	PSVPAG = psv_shadow;
}

void sinus(Sint16 x,Sint16*sin){
	unsigned psv_shadow;
	psv_shadow = PSVPAG;
	PSVPAG = __builtin_psvpage (sinr);
	*sin=sinr[x];
	PSVPAG = psv_shadow;
}

